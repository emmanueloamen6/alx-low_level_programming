#include <stdio.h>
/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */
int add(int a, int b)
{
	return a + b;
}

/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */
int sub(int a, int b)
{
        return a - b;
}
/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */
int mul(int a, int b)
{
        return a * b;
}

/**
 * div - division two integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */
int div(int a, int b)
{
        return a / b;
}

/**
 * mod - modulus two integers
 * @a: first integer
 * @b: second integer
 * Return: answer
 */
int mod(int a, int b)
{
        return a % b;
}
